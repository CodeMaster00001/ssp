// Function to redirect to a specified Amazon URL
function redirectToAmazon(url) {
    // Use window.location.href to redirect to the specified Amazon URL
    window.location.href = url;
}

// Example usage:
const amazonURL = 'https://www.amazon.com/'; // Specify the Amazon URL you want to redirect to

// Call the function to redirect to the specified Amazon URL
redirectToAmazon(amazonURL);
